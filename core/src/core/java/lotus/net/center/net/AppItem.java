package lotus.net.center.net;

public class AppItem {
	private int appIndex;
	private String appName;
	private String appAddress;
	private String appAdImageName;
	private float appProportion;
	public int getAppIndex() {
		return appIndex;
	}
	public void setAppIndex(int appIndex) {
		this.appIndex = appIndex;
	}
	public String getAppName() {
		return appName;
	}
	public void setAppName(String appName) {
		this.appName = appName;
	}
	public String getAppAddress() {
		return appAddress;
	}
	public void setAppAddress(String appAddress) {
		this.appAddress = appAddress;
	}
	public float getAppProportion() {
		return appProportion;
	}
	public void setAppProportion(float appProportion) {
		this.appProportion = appProportion;
	}
	public String getAppAdImageName() {
		return appAdImageName;
	}
	public void setAppAdImageName(String appAdImageName) {
		this.appAdImageName = appAdImageName;
	}
}
